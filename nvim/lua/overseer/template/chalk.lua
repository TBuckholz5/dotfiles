-- Detects the chalk monorepo root by walking up from cwd.
-- Falls back to ~/Development/chalk/chalk-private if not found.
local function get_chalk_root()
  local path = vim.fn.getcwd()
  while path and path ~= '/' do
    if vim.fn.isdirectory(path .. '/go-api-server') == 1
      and vim.fn.isdirectory(path .. '/engine') == 1 then
      return path
    end
    local parent = vim.fn.fnamemodify(path, ':h')
    if parent == path then break end
    path = parent
  end
  return vim.fn.expand '~/Development/chalk/chalk-private'
end

