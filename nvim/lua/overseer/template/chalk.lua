    'chalk: grpc-engine-query-server',
    'chalk: engine-branch-server',
    'chalk: go-api-server',
    'chalk: caddy',
    'chalk: frontend',
  }
  for _, name in ipairs(services) do
    overseer.run_template { name = name }
  end
end, {})

